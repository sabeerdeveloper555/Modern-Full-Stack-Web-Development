// Write a program to check grade
let gradeScore = Number(prompt("Enter you grade: "));
if (gradeScore >= 90) {
    console.log("Grade A");
} else if (gradeScore >= 75) {
    console.log("Grade B");
} else if (gradeScore >= 60) {
    console.log("Grade C");
} else {
    console.log("Fail");
}